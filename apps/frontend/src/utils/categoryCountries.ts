export function getCategoryCountryName(categoryId: string): string {
  // Try exact match first
  if (categoryCountries[categoryId]) {
    return categoryCountries[categoryId];
  }

  // Try prefix match (e.g. cs.AI -> cs)
  const prefix = categoryId.split(".")[0];
  if (categoryCountries[prefix]) {
    return categoryCountries[prefix];
  }

  // Fallback
  return `Unbekanntes Land (${categoryId})`;
}
